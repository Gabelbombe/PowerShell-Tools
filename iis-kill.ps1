# --------------------------------------------------------------------
# List available IIS sites.
# --------------------------------------------------------------------
Import-Module "Webadministration"
Get-ChildItem -Path IIS:\Sites | Format-Table -AutoSize
Write-Host ''

# --------------------------------------------------------------------
# Define the site to kill.
# --------------------------------------------------------------------
[string]$InetSiteName = $( Read-Host "Site Name" )

# --------------------------------------------------------------------
# Check for empty.
# --------------------------------------------------------------------
if(-not($InetSiteName)) { Throw "Site name cannot be empty..." }

# --------------------------------------------------------------------
# Drop site.
# --------------------------------------------------------------------
Remove-Item IIS:\Sites\$InetSiteName